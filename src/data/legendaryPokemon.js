// Combined list for easy searching
export const specialPokemon = [...legendaryPokemon, ...megaPokemon];

// Helper function to check if a Pokemon is special
export function isSpecialPokemon(id) {
  return specialPokemon.some(p => p.id === id || p.baseId === id);
}

// Helper function to get Pokemon category
export function getPokemonCategory(id) {
  const pokemon = specialPokemon.find(p => p.id === id || p.baseId === id);
  return pokemon ? pokemon.category : 'regular';
}